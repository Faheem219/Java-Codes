public class ifThenElse {
    public static void main(String[] args) {
        int myScore = 100;

        if(myScore>100){
            System.out.println("Great!");
        }
        else if(myScore>50){
            System.out.println("Good.");
        }
        else{
            System.out.println("Meh...");
        }
    }
}
